local function row(id, en, mp_cost, smn_lv, kind)
    local levels = {}
    if smn_lv and smn_lv > 0 then
        levels[15] = smn_lv
    end
    local t = (kind == "ward") and "BloodPactWard" or "BloodPactRage"
    return {
        id = id,
        en = en,
        ja = "",
        cast_time = 0,
        element = 0,
        icon_id = -1,
        icon_id_nq = 0,
        levels = levels,
        mp_cost = mp_cost,
        prefix = "/pet",
        range = 0,
        recast = 0,
        recast_id = id,
        requirements = 0,
        skill = 38,
        targets = 32,
        type = t,
    }
end

